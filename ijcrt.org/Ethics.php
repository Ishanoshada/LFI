		To maintain fair practice we at IJCRT strongly believe in following these guidelines. We are committed to fair practice of publication. 
Success in this regard can be achieved if ethics are well practiced by following participants:</br>
</br>
(1) Authors, (2) Editors, (3) Reviewers & (4) Publisher </br>
</br>
Authors: Authors should present an objective discussion of the significance of research work as well as sufficient detail and references to permit others to replicate the experiments. Fraudulent or knowingly inaccurate statements constitute unethical behavior and are unacceptable. Review articles should also be objective, comprehensive, and accurate accounts of the state of the art. The authors should ensure that their work is entirely original works, and if the work and/or words of others have been used, this has been appropriately acknowledged. Plagiarism in all its forms constitutes unethical publishing behavior and is unacceptable. Submitting the same manuscript to more than one journal concurrently constitutes unethical publishing behavior and is unacceptable. Authors should not submit articles describing essentially the same research to more than one journal. The corresponding author should ensure that there is a full consensus of all co-authors in approving the final version of the paper and its submission for publication. 
</br></br>
Duties of authors:
</br>
</br>	Reporting standards

Authors of reports of original research should present an accurate account of the work performed as well as an objective discussion of its significance. Underlying data should be represented accurately in the paper. A paper should contain sufficient detail and references to permit others to replicate the work. Fraudulent or knowingly inaccurate statements constitute unethical behavior and are unacceptable. Review and professional publication articles should also be accurate and objective, and editorial 'opinion' works should be clearly identified as such.
</br>
</br>Data access and retention

Authors may be asked to provide the raw data in connection with a paper for editorial review, and should in any event be prepared to retain such data for a reasonable time after publication.
</br>
</br>	Originality and plagiarism
</br>
The authors should ensure that they have written entirely original works, and if the authors have used the work and/or words of otherâ€™ that this has been appropriately cited or quoted. Plagiarism takes many forms, from 'passing off' another's paper as the author's own paper, to copying or paraphrasing substantial parts of another's paper (without attribution), to claiming results from research conducted by others. Plagiarism in all its forms constitutes unethical publishing behavior and is unacceptable. 
</br>
</br>	Multiple, redundant or concurrent publication
</br>
An author should not in general publish manuscripts describing essentially the same research in more than one journal or primary publication. Submitting the same manuscript to more than one journal concurrently constitutes unethical publishing behavior and is unacceptable. In general, an author should not submit for consideration in another journal a previously published paper.
</br>
</br>	Acknowledgement of sources
</br>
Proper acknowledgment of the work of others must always be given. Authors should cite publications that have been influential in determining the nature of the reported work. Information obtained privately, as in conversation, correspondence, or discussion with third parties, must not be used or reported without explicit, written permission from the source. Information obtained in the course of confidential services, such as refereeing manuscripts or grant applications, must not be used without the explicit written permission of the author of the work involved in these services.
</br>
</br>	Authorship of the paper
</br>
Authorship should be limited to those who have made a significant contribution to the conception, design, execution, or interpretation of the reported study. All those who have made significant contributions should be listed as co-authors. Where there are others who have participated in certain substantive aspects of the research project, they should be acknowledged or listed as contributors. The corresponding author should ensure that all co-authors have seen and approved the final version of the paper and have agreed to its submission for publication.
</br>
</br>	Hazards and human or animal subjects
</br>
If the work involves chemicals, procedures or equipment that have any unusual hazards inherent in their use, the author must clearly identify these in the manuscript. If the work involves the use of animal or human subjects, the author should ensure that the manuscript contains a statement that all procedures were performed in compliance with relevant laws and institutional guidelines and that the appropriate institutional committee(s) has approved them. Authors should include a statement in the manuscript that informed consent was obtained for experimentation with human subjects. The privacy rights of human subjects must always be observed.
</br>
</br>	Disclosure and conflicts of interest
</br>
All authors should disclose in their manuscript any financial or other substantive conflict of interest that might be construed to influence the results or interpretation of their manuscript. All sources of financial support for the project should be disclosed. Examples of potential conflicts of interest which should be disclosed include employment, consultancies, stock ownership, honoraria, paid expert testimony, patent applications/registrations, and grants or other funding. Potential conflicts of interest should be disclosed at the earliest stage possible.
</br>
</br>	Fundamental errors in published works
</br>
When an author discovers a significant error or inaccuracy in his/her own published work, it is the author's obligation to promptly notify the journal editor or publisher and cooperate with the editor to retract or correct the paper. If the editor or the publisher learns from a third party that a published work contains a significant error, it is the obligation of the author to promptly retract or correct the paper or provide evidence to the editor of the correctness of the original paper.
</br></br>
2. Editors: Editors should evaluate manuscripts exclusively on the basis of their academic merit. An editor must not use unpublished information in the editor's own research without the express written consent of the author. Editors should take reasonable responsive measures when ethical complaints have been presented concerning a submitted manuscript or published paper. 
</br>
Duties of editors:
</br>
</br>	Publication decisions
</br>
The editor of a peer-reviewed journal is responsible for deciding which of the articles submitted to the journal should be published, often working in conjunction with the relevant society (for society-owned or sponsored journals). The validation of the work in question and its importance to researchers and readers must always drive such decisions. The editor may be guided by the policies of the journal's editorial board and constrained by such legal requirements as shall then be in force regarding libel, copyright infringement and plagiarism. The editor may confer with other editors or reviewers (or society officers) in making this decision.
</br>
</br>	Fair play
</br>
An editor should evaluate manuscripts for their intellectual content without regard to race, gender, sexual orientation, religious belief, ethnic origin, citizenship, or political philosophy of the authors.
</br>
</br>	Confidentiality
</br>
The editor and any editorial staff must not disclose any information about a submitted manuscript to anyone other than the corresponding author, reviewers, potential reviewers, other editorial advisers, and the publisher, as appropriate.
</br>
</br>	Disclosure and conflicts of interest
</br>
Unpublished materials disclosed in a submitted manuscript must not be used in an editor's own research without the express written consent of the author. Privileged information or ideas obtained through peer review must be kept confidential and not used for personal advantage. Editors should recues themselves (i.e. should ask a co-editor, associate editor or other member of the editorial board instead to review and consider) from considering manuscripts in which they have conflicts of interest resulting from competitive, collaborative, or other relationships or connections with any of the authors, companies, or (possibly) institutions connected to the papers. Editors should require all contributors to disclose relevant competing interests and publish corrections if competing interests are revealed after publication. If needed, other appropriate action should be taken, such as the publication of a retraction or expression of concern. It should be ensured that the peer-review process for sponsored supplements is the same as that used for the main journal. Items in sponsored supplements should be accepted solely on the basis of academic merit and interest to readers and not be influenced by commercial considerations. Non-peer reviewed sections of their journal should be clearly identified.
</br>
</br>	Involvement and cooperation in investigations
</br>
An editor should take reasonably responsive measures when ethical complaints have been presented concerning a submitted manuscript or published paper, in conjunction with the publisher (or society). Such measures will generally include contacting the author of the manuscript or paper and giving due consideration of the respective complaint or claims made, but may also include further communications to the relevant institutions and research bodies, and if the complaint is upheld, the publication of a correction, retraction, expression of concern, or other note, as may be relevant. Every reported act of unethical publishing behavior must be looked into, even if it is discovered years after publication.
</br></br>
3. Reviewers: Any manuscripts received for review must be treated as confidential documents. Privileged information or ideas obtained through peer review must be kept confidential and not used for personal advantage. Reviews should be conducted objectively, and observations should be formulated clearly with supporting arguments, so that authors can use them for improving the paper. Any selected referee who feels unqualified to review the research reported in a manuscript or knows that its prompt review will be impossible should notify the editor and excuse himself from the review process. Reviewers should not consider manuscripts in which they have conflicts of interest resulting from competitive, collaborative, or other relationships or connections with any of the authors, companies, or institutions connected to the papers. 
</br>
Duties of reviewers:
</br>
</br>	Contribution to editorial decisions
</br>
Peer review assists the editor in making editorial decisions and through the editorial communications with the author may also assist the author in improving the paper. Peer review is an essential component of formal scholarly communication, and lies at the heart of the scientific method. Academy Publisher shares the view of many that all scholars who wish to contribute to publications have an obligation to do a fair share of reviewing.
</br>
</br>	Promptness

Any selected referee who feels unqualified to review the research reported in a manuscript or knows that its prompt review will be impossible should notify the editor and excuse himself from the review process.
</br>
</br>	Confidentiality
</br>
Any manuscripts received for review must be treated as confidential documents. They must not be shown to or discussed with others except as authorized by the editor.
</br>
</br>	Standards of objectivity
</br>
Reviews should be conducted objectively. Personal criticism of the author is inappropriate. Referees should express their views clearly with supporting arguments.
</br>
</br>	Acknowledgement of sources
</br>
Reviewers should identify relevant published work that has not been cited by the authors. Any statement that an observation, derivation, or argument had been previously reported should be accompanied by the relevant citation. A reviewer should also call to the editor's attention any substantial similarity or overlap between the manuscript under consideration and any other published paper of which they have personal knowledge.
</br>
</br>	Disclosure and conflict of interest
</br>
Unpublished materials disclosed in a submitted manuscript must not be used in a reviewer's own research without the express written consent of the author. Privileged information or ideas obtained through peer review must be kept confidential and not used for personal advantage. Reviewers should not consider manuscripts in which they have conflicts of interest resulting from competitive, collaborative, or other relationships or connections with any of the authors, companies, or institutions connected to the papers.
</br></br>
 4. Publisher: IJCRT does not allow any form of plagiarism. Plagiarism is considered to be a serious breach of scientific ethics by the entire scientific community. Incidents of plagiarism in a manuscript or published paper whether detected or reported, will be dealt seriously. We constantly support/advise and take suggestions from our Editors/Editorial Board/Reviewers on avoiding any malpractice of publication ethics.
	</br>

The publication of an article in a peer-reviewed journal is an essential building block in the development of a coherent and respected network of knowledge. It is a direct reflection of the quality of work of the author and the institutions that support them. Peer-reviewed articles support and embody the scientific method. It is therefore important to agree upon standards of expected ethical behavior.
</br></br>

Publication Ethics Policy contain below step</br>

1. Authorship of the paper</br>
2. Originality and plagiarism</br>
3. Data access and retention</br>
4. Multiple, redundant or concurrent publication.</br>
5. Acknowledgment of sources</br>
6. Disclosure and conflicts of interest</br>
7. Fundamental errors in published works.</br>
8. Reporting standards.</br>
9. Hazards and human or animal subjects.</br>
10. Use of patient images or case details.	</br>
