Open access refers to the practice of making peer-reviewed scholarly research and literature freely available online to anyone interested in reading it. Open access has two different versions—gratis and libre. Gratis open access is simply making research available for others to read without having to pay for it. However, it does not grant the user the right to make copies, distribute, or modify the work in any way beyond fair use. Libre open access is gratis, meaning the research is available free of charge, but it goes further by granting users additional rights, usually via a Creative Commons license, so that people are free to reuse and remix the research. 
</br>There are varying degrees of what may be considered Libre open access. For example, some scholarly articles may permit all uses except commercial use, some may permit all uses except derivative works, and some may permit all uses and simply require attribution. While some would argue that Libre open access should be free of any copyright restrictions (except attribution), other scholars consider a work that removes at least some permission barriers to be libre. In addition to the gratis/libre distinction regarding users’ rights to a work, there are also distinctions regarding the venue in which open access works are published and archived—green and gold. Green open access involves authors self-archiving their articles by sharing them on their own website, or more preferably, in their institution's Institutional Repository or in some other public archive. 
</br>Gold open access articles are published in a journal that is open access, which means the journal will handle hosting and distributing the journal article in a free and open manner. Gold open access can come with a cost—some gold open access journals have publication fees that need to paid by the author (or the author's employer) to cover the cost of publishing the article. These publication fees can be costly, but thankfully less than a third of gold open access journals have a publication fee. Currently, most Gold open access journals are still publishing gratis (free of charge to read) as opposed to libre (free of charge to read, copy, distribute, etc).
